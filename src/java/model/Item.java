package model;

/**
 *
 * @author rwspa
 */
public class Item {
    private String op;
    private int qtd;
    
    public Item(){
        
    }

    public Item(String op, int qtd) {
        this.op = op;
        this.qtd = qtd;
    }

    /**
     * @return the op
     */
    public String getOp() {
        return op;
    }

    /**
     * @param op the op to set
     */
    public void setOp(String op) {
        this.op = op;
    }

    /**
     * @return the qtd
     */
    public int getQtd() {
        return qtd;
    }

    /**
     * @param qtd the qtd to set
     */
    public void setQtd(int qtd) {
        this.qtd = qtd;
    }
    
    
}
